    public QaRetrievalLogContext {
        semanticStateVersion = hasText(semanticStateVersion) ? semanticStateVersion : SessionSemanticState.VERSION;
        semanticStateJson = hasText(semanticStateJson)
                ? semanticStateJson
                : SessionSemanticState.fromTopicFields(
                        resolvedTopic,
                        "",
                        topicSource,
                        topicConfidence,
                        topicStackJson
                ).json();
        topicEntityBindingApplied = Boolean.TRUE.equals(topicEntityBindingApplied);
        topicEntityBindingStatus = hasText(topicEntityBindingStatus) ? topicEntityBindingStatus : "skipped";
        topicEntityBindingStrategy = hasText(topicEntityBindingStrategy) ? topicEntityBindingStrategy : "none";
        topicEntityCandidateCount = topicEntityCandidateCount == null ? 0 : Math.max(0, topicEntityCandidateCount);
        topicEntityCandidatesJson = hasText(topicEntityCandidatesJson) ? topicEntityCandidatesJson : "[]";
        topicEntityLookupDurationMs = topicEntityLookupDurationMs == null ? 0L : Math.max(0L, topicEntityLookupDurationMs);
        memoryGovernanceVersion = hasText(memoryGovernanceVersion) ? memoryGovernanceVersion : "memory-governance-v1";
        memoryLongTermCount = memoryLongTermCount == null ? 0 : Math.max(0, memoryLongTermCount);
        memoryRecentHistoryCount = memoryRecentHistoryCount == null ? 0 : Math.max(0, memoryRecentHistoryCount);
        memorySourcesJson = hasText(memorySourcesJson) ? memorySourcesJson : "[]";
    }

