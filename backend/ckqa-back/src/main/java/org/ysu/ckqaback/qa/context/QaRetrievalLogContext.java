    private static Integer estimateTokens(BudgetSizeEstimate estimate) {
        return estimate == null ? null : estimate.tokens();
    }

    private static String estimateTokenizer(BudgetSizeEstimate estimate) {
        return estimate == null ? null : estimate.tokenizer();
    }

    private static String estimateFallbackReason(BudgetSizeEstimate estimate) {
        return estimate == null ? null : estimate.fallbackReason();
    }

    private static boolean hasText(String value) {
        return value != null && !value.trim().isEmpty();
    }
